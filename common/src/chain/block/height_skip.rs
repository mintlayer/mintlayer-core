use crate::primitives::BlockHeight;

fn invert_lowest_one(n: u64) -> u64 {
    n & n.wrapping_sub(1)
}

pub fn get_skip_height(h: BlockHeight) -> BlockHeight {
    let height: u64 = h.into();
    if height < 2 {
        return BlockHeight::new(0);
    }

    // Determine which height to jump back to. Any number strictly lower than
    // height is acceptable, but the following expression seems to perform well in
    // simulations (max 110 steps to go back up to 2**18 blocks).
    let res = if height & 1 != 0 {
        invert_lowest_one(invert_lowest_one(height - 1)) + 1
    } else {
        invert_lowest_one(height)
    };
    BlockHeight::new(res)
}

